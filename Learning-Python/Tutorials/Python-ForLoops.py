### --- For Loops --- ###

# This will print 0, 2, 4, 6, 8, 10 
for x in range(0, 11, 2): # Start, Stop, Step
    print(x)

# This will print 1, 2, 3, 4, 5 
for x in range(0, 6, 1): # Stop
    print(x)

# This will print 1-9
for x in range(0, 10):    # Start, Stop
    print(x)

# This will print 5, 10
for x in range(0, 10, 5):
    print('Hello')